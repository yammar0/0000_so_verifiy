# 3. 台本パース関数
# ============================================================
def parse_script(text):
    """
    台本から「ナビゲーターA：」「ナビゲーターB：」「A：」「B：」を検出し、
    各セリフを (話者, セリフ) のリストとして返す。
    """
    pattern = re.compile(
        r'(?P<speaker>ナビゲーターA|ナビゲーターB|A|B)[：:]\s*(?P<dialogue>.*?)(?=(?:ナビゲーターA|ナビゲーターB|A|B)[：:]|$)',
        re.DOTALL
    )
    matches = pattern.findall(text)
    
    dialogue_list = []
    for speaker, dialogue in matches:
        if speaker in ('ナビゲーターA', 'A'):
            speaker = 'A'
        elif speaker in ('ナビゲーターB', 'B'):
            speaker = 'B'
        else:
            continue
        dialogue = dialogue.strip()
        if dialogue:
            dialogue_list.append((speaker, dialogue))
    return dialogue_list

# ============================================================
# 4. TTS合成関数（1セリフずつ）
# ============================================================
def synthesize_speech(text, voice_name, gender, client):
    """
    Google TTS を呼び出し、音声データ（MP3バイト）を返す。
    """
    input_text = texttospeech.SynthesisInput(text=text)
    voice = texttospeech.VoiceSelectionParams(
        language_code="ja-JP",
        name=voice_name,
        ssml_gender=gender
    )
    audio_config = texttospeech.AudioConfig(
        audio_encoding=texttospeech.AudioEncoding.MP3,
        speaking_rate=1.0,
        pitch=0.0
    )
    response = client.synthesize_speech(
        request={"input": input_text, "voice": voice, "audio_config": audio_config}
    )
    return response.audio_content

