# 5. メイン処理（デバッグコード追加済み）
# ============================================================
def main():
    client = texttospeech.TextToSpeechClient()

    voice_config = {
        'A': ('ja-JP-Neural2-B', texttospeech.SsmlVoiceGender.FEMALE),
        'B': ('ja-JP-Neural2-D', texttospeech.SsmlVoiceGender.MALE),
    }

    dialogues = parse_script(SCRIPT_TEXT)

    # ================== デバッグコード ここから ==================
    print(f"DEBUG: 合計 {len(dialogues)} 個のセリフを検出しました。")
    speakers = set([s for s, _ in dialogues])
    print(f"DEBUG: 検出された話者: {speakers}")
    # 最初の5つのセリフを表示
    for i, (s, t) in enumerate(dialogues[:5]):
        print(f"DEBUG {i}: {s} -> {t[:20]}...")
    # ================== デバッグコード ここまで ==================

    if not dialogues:
        print("エラー: 台本のパースに失敗しました。話者ラベルを確認してください。")
        return

    print(f"合計 {len(dialogues)} 個のセリフを合成します。")

    audio_segments = []
    for idx, (speaker, text) in enumerate(dialogues, 1):
        print(f"  [{idx}/{len(dialogues)}] {speaker}: {text[:20]}...")
        try:
            voice_name, gender = voice_config[speaker]
            audio_bytes = synthesize_speech(text, voice_name, gender, client)
            segment = AudioSegment.from_mp3(io.BytesIO(audio_bytes))
            audio_segments.append(segment)
        except Exception as e:
            print(f"  エラー: {e}")

    if audio_segments:
        combined = AudioSegment.empty()
        for seg in audio_segments:
            combined += seg
        output_file = "output_audio.mp3"
        combined.export(output_file, format="mp3")
        print(f"\n✅ 音声ファイルを生成しました: {output_file} (サイズ: {len(combined)} ミリ秒)")
    else:
        print("エラー: 合成された音声セグメントがありません。")

if __name__ == "__main__":
    main()