def setup_credentials():
    """
    Renderの環境変数 GOOGLE_APPLICATION_CREDENTIALS_JSON から
    JSON鍵の中身を読み取り、一時ファイルを作成して
    GOOGLE_APPLICATION_CREDENTIALS にそのパスを設定する。
    """
    creds_json = os.environ.get('GOOGLE_APPLICATION_CREDENTIALS_JSON')
    if creds_json:
        try:
            json.loads(creds_json)
        except json.JSONDecodeError:
            print("エラー: GOOGLE_APPLICATION_CREDENTIALS_JSON が正しいJSON形式ではありません。")
            return

        temp_file = tempfile.NamedTemporaryFile(
            mode='w',
            suffix='.json',
            delete=False,
            encoding='utf-8'
        )
        temp_file.write(creds_json)
        temp_file.close()
        
        os.environ['GOOGLE_APPLICATION_CREDENTIALS'] = temp_file.name
        
        def cleanup():
            if os.path.exists(temp_file.name):
                os.unlink(temp_file.name)
        atexit.register(cleanup)
        
        print(f"認証情報を一時ファイルに設定しました: {temp_file.name}")
    else:
        print("GOOGLE_APPLICATION_CREDENTIALS_JSON が未設定です。")
        print("ローカル環境では GOOGLE_APPLICATION_CREDENTIALS のパスを直接設定してください。")

